// Generate random image URLs for cities we don't have specific data for
export function getDefaultImagesForCity(city) {
  const colors = ['blue', 'green', 'red', 'purple', 'orange', 'pink']
  const images = []
  for (let i = 0; i < 5; i++) {
    const color = colors[Math.floor(Math.random() * colors.length)]
    images.push(`https://via.placeholder.com/600x400?text=${encodeURIComponent(city)}&bg=${color}`)
  }
  return images
}

// Get images for a specific listing slug
export function getImagesForListing(slug) {
  if (TRIPADVISOR_IMAGES[slug]) {
    return TRIPADVISOR_IMAGES[slug]
  }
  
  // For city-based slugs, try to extract city name and generate placeholder images
  const cityMatch = slug.match(/([a-z-]+)(?:-manila|-makati)?$/)
  if (cityMatch) {
    const cityName = cityMatch[1].replace(/-/g, ' ')
    return getDefaultImagesForCity(cityName)
  }
  
  return []
}

// Function to enrich a listing with images
export function enrichListingWithImages(listing) {
  const images = getImagesForListing(listing.slug)
  return {
    ...listing,
    images: images.length > 0 ? images : (listing.images || [])
  }
}

