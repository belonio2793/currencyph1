export function getCityByName(name) {
  return PHILIPPINES_CITIES.find(city => 
    city.toLowerCase() === name.toLowerCase()
  )
}

export function searchCities(query) {
  if (!query.trim()) return PHILIPPINES_CITIES
  return PHILIPPINES_CITIES.filter(city =>
    city.toLowerCase().includes(query.toLowerCase())
  )
}
